public class pro33 {
    public static void main(String[] args) {
        //second
        int sum=0;
        int n=0;
        while(n<=3){
            System.out.print("The even number is:- "+2*n);
            System.out.println();
            sum += 2*n;
            n++; 
        }
        System.out.println("The sum of even numbers are:- "+sum);
        
    }
    
}
